#Lesson 1: Control Flow with if / else and Conditional Operators
#Operator         Meaning
#  >              Greater than
#  <              Less than
# >=              Greater than or equal to
# <=              Less than or equal to
# ==              Equal to
# !=              Not equal to

print("Welcome to the rollercoaster!")
height = int(input("What is your height in cm? "))

if height >= 120:
    print("You can ride the rollercoaster!")
else:
    print("Sorry, you're not tall enough for this ride.")
